package day01;

public class Loops {

    public static void main(String[] args) {

        for(int i = 0; i < 5; i++ ){
            System.out.println("EU5");
        }

        System.out.println("-----------------------------");

        int i = 0;
        while(i < 5){
            System.out.println("EU5");
            i++;
        }


    }

}
